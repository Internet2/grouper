package edu.internet2.middleware.ldappc.exception;

/**
 * This is the parent class to all non-runtime Ldappc exceptions.
 */
public class LdappcException extends RuntimeException {

  private static final long serialVersionUID = 5129237037294355217L;

  public LdappcException() {
    super();
  }

  public LdappcException(String message) {
    super(message);
  }

  public LdappcException(String message, Throwable cause) {
    super(message, cause);
  }

  public LdappcException(Throwable cause) {
    super(cause);
  }

}
