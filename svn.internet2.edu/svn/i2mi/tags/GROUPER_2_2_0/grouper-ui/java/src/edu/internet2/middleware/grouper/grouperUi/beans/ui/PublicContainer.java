package edu.internet2.middleware.grouper.grouperUi.beans.ui;


/**
 * container for public screens (e.g. error screen)
 */
public class PublicContainer {

  /**
   * error code for error screen
   */
  private String errorCode;

  
  /**
   * error code for error screen
   * @return the errorCode
   */
  public String getErrorCode() {
    return this.errorCode;
  }

  
  /**
   * error code for error screen
   * @param errorCode1 the errorCode to set
   */
  public void setErrorCode(String errorCode1) {
    this.errorCode = errorCode1;
  }
  
}
